def spec_classifier(text):
    text_lower = text.lower()

    # Разбиваем на слова (учитываем русские буквы)
    words = re.findall(r'[а-яё]+', text_lower)

    # Проверяем каждое слово
    for word in words:
        for key, val in FAST_KEYWORDS_SPEC.items():
            for i in val:
                if i == word:
                    return key
    return 'all'

